// Terrain tiles, 16×16, four shades: 0 lightest … 3 darkest, '.' transparent.
// Animated tiles provide a ".b" second frame; textured tiles a ".2" variant.

import type { SpriteDef } from "../core/sprites";

const fill = (ch: string): SpriteDef => Array(16).fill(ch.repeat(16));

// Stamp overlay rows onto a base sprite (non-space characters win).
function stamp(base: SpriteDef, overlay: Record<number, string>): SpriteDef {
	return base.map((row, y) => {
		const over = overlay[y];
		if (!over) return row;
		let out = "";
		for (let x = 0; x < 16; x++) {
			const ch = over[x];
			out += ch && ch !== " " ? ch : row[x]!;
		}
		return out;
	});
}

